def count_word_occurrences(words):
    # Создаем пустой словарь для хранения количества повторений каждого слова
    word_count = {}

    # Проходим по каждому слову в списке
    for word in words:
        # Используем метод get() для получения текущего количества повторений слова
        # Если слово не найдено, get() вернет 0, иначе увеличиваем текущий счетчик на 1
        word_count[word] = word_count.get(word, 0) + 1

    # Возвращаем словарь с количеством повторений слов
    return word_count